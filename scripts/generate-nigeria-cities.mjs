function normalizeStateName(name) {
  return STATE_ALIASES[name] ?? name;
}

function cityLooseKey(name) {
  return name.trim().toLowerCase().replace(/[^a-z0-9]/g, "");
}

/** Collapse spelling variants to one canonical label (supplements win). */
const CITY_ALIASES = {
  badagary: "Badagry",
  amuwoodofin: "Amuwo-Odofin",
  ibejulekki: "Ibeju-Lekki",
  ifakoijaye: "Ifako-Ijaiye",
  oshodiisolo: "Oshodi-Isolo"
};

function canonicalCityName(name) {
  const loose = cityLooseKey(name);
  return CITY_ALIASES[loose] ?? name.trim();
}

function mergeCities(state, lgas) {
  const extra = SUPPLEMENTARY_CITIES[state] ?? [];
  const seen = new Set();
  const list = [];
  for (const name of [...extra, ...lgas]) {
    const trimmed = canonicalCityName(name);
    if (!trimmed) continue;
    const key = cityLooseKey(trimmed);
    if (seen.has(key)) continue;
    seen.add(key);
    list.push(trimmed);
  }
  list.sort((a, b) => a.localeCompare(b, "en", { sensitivity: "base" }));
  return list;
}

function main() {
  if (!fs.existsSync(lgasPath)) {
    console.error(`Missing ${lgasPath}. Download lgas.json from nigeria-geojson-data first.`);
    process.exit(1);
  }

  const raw = JSON.parse(fs.readFileSync(lgasPath, "utf8"));
  const merged = {};
  let total = 0;

  for (const [rawState, lgas] of Object.entries(raw)) {
    const state = normalizeStateName(rawState);
    merged[state] = mergeCities(state, lgas);
    total += merged[state].length;
  }

  const lines = [
    "/**",
    " * Nigerian cities and LGAs per state (36 states + FCT).",
    " * Generated by scripts/generate-nigeria-cities.mjs — do not edit by hand.",
    " */",
    "export const NIGERIA_STATE_CITIES: Record<string, readonly string[]> = {"
  ];

  for (const state of Object.keys(merged).sort((a, b) => a.localeCompare(b, "en"))) {
    const cities = merged[state].map((c) => JSON.stringify(c)).join(", ");
    lines.push(`  ${JSON.stringify(state)}: [${cities}],`);
  }

  lines.push("};", "");
  fs.writeFileSync(outPath, lines.join("\n"));

  console.log(`Wrote ${outPath}`);
  console.log(`  ${Object.keys(merged).length} states, ${total} city entries (${raw && Object.values(raw).flat().length} LGAs + supplements)`);
}

main();
