#!/usr/bin/env node
/**
 * One-command Android release: bump versions, clean, build web, sync Capacitor, APK + AAB.
 *
 * Requires .env (VITE_SUPABASE_URL, VITE_SUPABASE_ANON_KEY) and android/key.properties.
 */
import { spawnSync } from "node:child_process";
import { existsSync, readFileSync, rmSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import dotenv from "dotenv";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
process.chdir(root);
dotenv.config();

const gradlePath = join(root, "android", "app", "build.gradle");
const buildInfoPath = join(root, "src", "buildInfo.ts");
const swPath = join(root, "public", "sw.js");
const appReleasePath = join(root, "src", "constants", "appRelease.ts");

const requiredEnv = ["VITE_SUPABASE_URL", "VITE_SUPABASE_ANON_KEY"];
const missingEnv = requiredEnv.filter((key) => !String(process.env[key] || "").trim());
if (missingEnv.length) {
  console.error(
    `\n[bamsignal] Missing ${missingEnv.join(", ")} in .env.\n` +
      "The Android app bundles the web build — set Supabase keys before release.\n"
  );
  process.exit(1);
}

const keyProps = join(root, "android", "key.properties");
if (!existsSync(keyProps)) {
  console.error(
    "\n[bamsignal] android/key.properties is missing.\n" +
      "See android/key.properties.example and ANDROID_RELEASE_NOTES.md\n"
  );
  process.exit(1);
}

function bumpVersionName(name) {
  const parts = String(name).trim().split(".");
  if (parts.length < 2) return `${name}.1`;
  const last = Number.parseInt(parts[parts.length - 1], 10);
  parts[parts.length - 1] = String(Number.isFinite(last) ? last + 1 : 1);
  return parts.join(".");
}

function readVersions() {
  const gradle = readFileSync(gradlePath, "utf8");
  const versionCode = Number.parseInt(gradle.match(/versionCode\s+(\d+)/)?.[1] ?? "0", 10);
  const versionName = gradle.match(/versionName\s+"([^"]+)"/)?.[1] ?? "1.0.0";
  return { versionCode, versionName };
}

function patchGradle(content, versionCode, versionName) {
  return content
    .replace(/versionCode\s+\d+/, `versionCode ${versionCode}`)
    .replace(/versionName\s+"[^"]+"/, `versionName "${versionName}"`);
}

function writeBuildInfo(versionName, versionCode, buildTime) {
  const cacheVersion = `bamsignal-v${versionName}-${versionCode}`;
  const body = `/** Generated by scripts/build-android-release.mjs — do not edit by hand before release. */
export const BUILD_VERSION = "${versionName}";
export const BUILD_CODE = "${versionCode}";
export const BUILD_TIME = "${buildTime}";
export const CACHE_VERSION = "${cacheVersion}";
`;
  writeFileSync(buildInfoPath, body, "utf8");

  const appReleaseBody = `/** Synced by scripts/build-android-release.mjs from src/buildInfo.ts */
import { BUILD_CODE, BUILD_TIME, BUILD_VERSION } from "../buildInfo";

export const APP_VERSION_NAME = BUILD_VERSION;
export const APP_VERSION_CODE = Number(BUILD_CODE);
export const APP_BUILD_VERSION = BUILD_VERSION;
export const APP_BUILD_CODE = BUILD_CODE;
export const APP_BUILD_TIME =
  typeof __APP_BUILD_TIME__ !== "undefined" ? __APP_BUILD_TIME__ : BUILD_TIME;
export const APP_BUILD_LABEL = \`Build \${BUILD_VERSION} (\${BUILD_CODE})\`;
`;
  writeFileSync(appReleasePath, appReleaseBody, "utf8");

  if (existsSync(swPath)) {
    const sw = readFileSync(swPath, "utf8");
    const nextSw = sw.replace(
      /const CACHE_NAME = "[^"]+";/,
      `const CACHE_NAME = "${cacheVersion}";`
    );
    writeFileSync(swPath, nextSw, "utf8");
  }

  return cacheVersion;
}

function cleanPaths() {
  const targets = [
    join(root, "dist"),
    join(root, "android", "app", "build"),
    join(root, "android", "app", "src", "main", "assets", "public")
  ];
  for (const target of targets) {
    if (!existsSync(target)) continue;
    rmSync(target, { recursive: true, force: true });
    console.log(`[bamsignal] Removed ${target.replace(root + "/", "")}`);
  }
}

function resolveJavaHome() {
  const candidates = [
    process.env.JAVA_HOME,
    "/Applications/Android Studio.app/Contents/jbr/Contents/Home",
    process.env.ANDROID_STUDIO_JBR
  ].filter(Boolean);

  for (const home of candidates) {
    const java = join(home, "bin", "java");
    if (!existsSync(java)) continue;
    const probe = spawnSync(java, ["-version"], { encoding: "utf8" });
    const text = `${probe.stderr || ""}${probe.stdout || ""}`;
    const match = text.match(/version "(\d+)/);
    const major = match ? Number(match[1]) : 0;
    if (major >= 17 && major <= 21) return home;
  }

  console.error(
    "\n[bamsignal] Need Java 17–21 for Gradle.\n" +
      'Set JAVA_HOME to Android Studio JBR, e.g.:\n' +
      'export JAVA_HOME="/Applications/Android Studio.app/Contents/jbr/Contents/Home"\n'
  );
  process.exit(1);
}

function run(cmd, args, extraEnv = {}, cwd = root) {
  const result = spawnSync(cmd, args, {
    stdio: "inherit",
    cwd,
    env: { ...process.env, ...extraEnv },
    shell: process.platform === "win32"
  });
  if (result.status !== 0) process.exit(result.status ?? 1);
}

const current = readVersions();
const nextVersionCode = current.versionCode + 1;
const nextVersionName = bumpVersionName(current.versionName);
const buildTime = new Date().toISOString();

console.log(
  `[bamsignal] Bumping ${current.versionName} (${current.versionCode}) → ${nextVersionName} (${nextVersionCode})`
);

const gradleContent = readFileSync(gradlePath, "utf8");
writeFileSync(gradlePath, patchGradle(gradleContent, nextVersionCode, nextVersionName), "utf8");

const cacheVersion = writeBuildInfo(nextVersionName, nextVersionCode, buildTime);
console.log(`[bamsignal] CACHE_VERSION = ${cacheVersion}`);

cleanPaths();

const javaHome = resolveJavaHome();
console.log(`[bamsignal] Using JAVA_HOME=${javaHome}`);

run("npm", ["run", "build"], {
  VITE_APP_BUILD_ID: cacheVersion,
  VITE_APP_BUILD_TIME: buildTime
});
run("npx", ["cap", "sync", "android"]);

const androidDir = join(root, "android");
const gradlew = process.platform === "win32" ? "gradlew.bat" : "./gradlew";
run(
  gradlew,
  ["clean", "bundleRelease", "assembleRelease"],
  {
    JAVA_HOME: javaHome,
    PATH: `${join(javaHome, "bin")}:${process.env.PATH || ""}`
  },
  androidDir
);

const apk = join(root, "android", "app", "build", "outputs", "apk", "release", "app-release.apk");
const aab = join(root, "android", "app", "build", "outputs", "bundle", "release", "app-release.aab");

if (!existsSync(apk) || !existsSync(aab)) {
  console.error("[bamsignal] Build finished but APK or AAB output is missing.");
  process.exit(1);
}

console.log("\nDONE");
console.log(`versionName: ${nextVersionName}`);
console.log(`versionCode: ${nextVersionCode}`);
console.log(`AAB: ${aab}`);
console.log(`APK: ${apk}`);
console.log("\nUpload the AAB to Google Play Console.");
console.log(`Install on device: adb install -r "${apk}"\n`);
